def compress(s):
    diff = -1
    while diff != 0:
        curr_len = len(s)
        print("Current length: %d" % curr_len)
        i = 0
        while i < (len(s) -1):
            if check_pair(s[i], s[i + 1]):
                s = s[:i] + s[i + 2:]
                i += 2
            else:
                i += 1
        diff = len(s) - curr_len
    return len(s)


